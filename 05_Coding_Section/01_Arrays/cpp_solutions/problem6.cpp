// ─── OPTIMAL SOLUTION ────────────────────────────────────
// Time: O(N), Space: O(1)
int maxSumSubarrayK(vector<int>& arr, int k) {
    int windowSum = 0, maxSum = INT_MIN;
    for (int i = 0; i < k; i++) windowSum += arr[i];
    maxSum = windowSum;
    for (int i = k; i < arr.size(); i++) {
        windowSum += arr[i] - arr[i - k];
        maxSum = max(maxSum, windowSum);
    }
    return maxSum;
}

